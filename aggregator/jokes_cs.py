def get_joke_for_day(day_of_year: int) -> str:
    "””Return a joke rotating by day of year (with year offset to vary across years).””"
    import datetime as _dt
    year_offset = _dt.datetime.now().year % len(JOKES_CZ)
    idx = (day_of_year + year_offset) % len(JOKES_CZ)
    return JOKES_CZ[idx]
